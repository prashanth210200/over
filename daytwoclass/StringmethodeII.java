package daytwoclass;

public class StringmethodeII {
	public static void main(String[] args) {
		String num="paramasivam";
		System.out.println(num.toUpperCase());
		System.out.println(num.toLowerCase());
		System.out.println(num.length());
		System.out.println(num.concat("thiyagu"));
		System.out.println(num.equals("Paramasivam"));
		System.out.println(num.equalsIgnoreCase("Paramasivam"));
		System.out.println(num.startsWith("pa"));
		System.out.println(num.endsWith("vam"));
		System.out.println(num.substring(2,5));
		System.out.println(num.contains("rama"));
		System.out.println(num.charAt(3));
		System.out.println(num.indexOf("m"));
		
		
	}

}
