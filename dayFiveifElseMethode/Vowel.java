package dayFiveifElseMethode;

public class Vowel {
	public static void main(String[] args) {
		String name="nisha";
		if(name.contains("a")||name.contains("e")||name.contains("i")||name.contains("o")) {
			System.out.println(name +" contains vowels");
		}
		else {
			System.out.println(name+" not contains vowels");
		}
	}

}
