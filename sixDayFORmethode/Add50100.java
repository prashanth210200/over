package sixDayFORmethode;

public class Add50100 {
	public static void main(String[] args) {
		int add=1;
		for(int i=50;i<=100;i++) {
			add=add+i;
			
	}
	System.out.println(add);
		
	for (int i=350;i>=200;i--) {
		add=add+i;
		}
		System.out.println(add);
		int mult=1;
		for(int i=5;i<=10;i++) {
			mult=mult*i;
			
		}
		System.out.println(mult);
	}

}
